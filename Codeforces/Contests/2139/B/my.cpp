void solve()
{
    // input here only
    int n, m;
    cin >> n >> m;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }

    sort(a.begin(), a.end());
    // take last m cakes
    ll ans = 0;
    int l = m;
    for (int i = n - 1; i >= 0 && l > 0; i--)
    {
        ans += a[i] * l;
        l--;
    }
    cout << ans << endl;
}

